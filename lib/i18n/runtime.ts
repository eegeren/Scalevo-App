export function translateText(value: string, lang: Lang): string {
  if (lang === "tr") return value;

  if (EXACT_UNICODE_TR_TO_EN[value]) return EXACT_UNICODE_TR_TO_EN[value];
  if (EXACT_TR_TO_EN[value]) return EXACT_TR_TO_EN[value];

  let result = value;
  result = result.replace(/(\d+)\s*ürün/g, "$1 products");
  result = result.replace(/(\d+)\s*analiz/g, "$1 analyses");
  result = result.replace(/%(\d+)\s*marj/g, "%$1 margin");
  result = result.replace(/hedef fiyata ulaşıldı/gi, "target price reached");
  result = result.replace(/piyasa fiyatı/gi, "market price");

  for (const [tr, en] of FRAGMENT_REPLACEMENTS.sort((a, b) => b[0].length - a[0].length)) {
    result = result.replaceAll(tr, en);
  }

  return result;
}
